#include "libft.h"
#include <unistd.h>

void		ft_put_size_t(size_t n)
{
	if (n >= 10)
	{
		ft_put_size_t(n / 10);
		ft_put_size_t(n % 10);
	}
	if (n < 10)
		ft_putchar(n + '0');
}
